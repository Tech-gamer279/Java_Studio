// Java is a high-level, object-oriented programming language.
// It was developed by Sun Microsystems in 1995.
// Java programs are platform-independent thanks to the Java Virtual Machine (JVM).
// Java is widely used for web, mobile, desktop, and enterprise applications.
// Its syntax is similar to C++ and emphasizes reliability, security, and portability.

// Printing Tutorials and Operations in Java:

// The main method is the entry point of any Java application.
public class HelloWorld {
    public static void main(String[] args) {
        // Printing text to the console
        System.out.println("Hello, World!");

        // Printing numbers and results of operations
        System.out.println(10); // prints a number
        System.out.println(5 + 3); // prints the result of addition

        // Printing variables
        int a = 7;
        int b = 3;
        System.out.println("a = " + a);
        System.out.println("b = " + b);

        // Printing the result of an operation
        System.out.println("a + b = " + (a + b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
    }
}
