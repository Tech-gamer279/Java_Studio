// Arithmetic Operations in Java:
// Java supports basic arithmetic operations: addition, subtraction, multiplication, division, and modulus.

public class ArithmeticExample {
    public static void main(String[] args) {
        int a = 15;
        int b = 4;

        System.out.println("Addition: " + (a + b));         // 19
        System.out.println("Subtraction: " + (a - b));      // 11
        System.out.println("Multiplication: " + (a * b));   // 60
        System.out.println("Division: " + (a / b));         // 3
        System.out.println("Modulus: " + (a % b));          // 3
    }
}
// This code demonstrates how to perform basic arithmetic operations in Java.
// The output will show the results of each operation performed on the integers a and b.
