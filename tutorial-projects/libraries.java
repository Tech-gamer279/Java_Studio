// Java Libraries:
// Libraries are collections of pre-written code that provide useful functionality for your programs.

// Common Java Libraries:
// 1. java.util - Provides data structures like ArrayList, HashMap, and utility classes.
// 2. java.io   - Used for input and output operations (reading/writing files).
// 3. java.math - Provides mathematical functions and classes.
// 4. java.net  - Used for networking operations (working with URLs, sockets).
// 5. java.time - Provides classes for date and time manipulation.

// Example using java.util.ArrayList:
import java.util.ArrayList;

public class LibrariesExample {
    public static void main(String[] args) {
        ArrayList<String> fruits = new ArrayList<>();
        fruits.add("Apple");
        fruits.add("Banana");
        fruits.add("Orange");
        System.out.println("Fruits: " + fruits);
    }
}

